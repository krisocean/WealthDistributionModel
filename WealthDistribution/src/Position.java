/**
 * A class record the position on the land
 */
public class Position {

    public int row;
    public int column;

    /**
     * record the position on the land
     *
     * @param row
     *            the row index of position
     * @param column
     *            the column index of position
     */
    public Position(int row, int column){
        this.row = row;
        this.column = column;
    }
}
